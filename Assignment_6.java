package assignments;
//Write a program on +,-,*,/ and % operators
public class Assignment_6 
{
	public static void main(String[] args) 
	{
		int a=10; // Addition operator (+)
		int b=11;
		int c=a+b;
		System.out.println(c);
		
		int e=10; // Subtraction operator (-)
		int f=11;
		int g=e-f;
		System.out.println(g);
		
		int h=10; // Multiplication operator (*)
		int i=11;
		int j=h*i;
		System.out.println(j);
		
		int k=10; // Division operator (/)
		int l=11;
		int m=k/l;
		System.out.println(m);
		
		int n=10; // Modulo operator (%)
		int o=11;
		int p=n%o;
		System.out.println(p);
		
	}
}
